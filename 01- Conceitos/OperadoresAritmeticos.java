public class OperadoresAritmeticos{

	//Iniciar o sistema
	public static void main(String[] args){

/* 
	Ordem aritmética
	1) Divisão
	2) Multiplicação
	3) Subtração
	4) Soma
*/

	//Concatenação = Unir informações

	//Soma
	System.out.println("O resultado da soma é: " + (5+4));

	//Subtração
	System.out.println("O resultado da subtração é: " + (5-4));

	//Multiplicação
	System.out.println("O resultado é:" + (5*4));

	//Resto de divisão
	System.out.println(5%4);

	//Divisão
	System.out.println("O resultado da divisão de 4 por 2 é : "+4/2);

	//Média
	System.out.println("A média é: "+ ((7+7+7)/3));	

	//Extrair percentual
	System.out.println("10% de 800 é: "+(800*0.10));
}
}