//Englobar códigos
public class OperadoresRelacionais{

	//Iniciar o sistema
	public static void main(String[] args){
	
	//Maior
	System.out.println(5>4);

	//Menor
	System.out.println(5<4);

	//Maior ou igual	
	System.out.println(5>=4);

	//Menor ou igual
	System.out.println(5<=4);

	//Igual
	System.out.println(5==4);

	//Diferente
	System.out.println(5!=4);	

	
}

}